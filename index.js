function add (a,b, result){
  result = a + b;
  return result;
}

function subtract (a,b, result){
  result = a - b;
  return result;
}

function multiply (a,b, result){
  result = a * b;
  return result;
}

function divide (a,b, result){
  result = a / b;
  return result;
}

