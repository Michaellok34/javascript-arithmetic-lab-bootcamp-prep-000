function add (a,b, result){
  result = a + b;
  return result;
}

function subtracts (a,b, result){
  result = a - b;
  return result;
}

function multiplies (a,b, result){
  result = a * b;
  return result;
}

function divides (a,b, result){
  result = a / b;
  return result;
}

